//xがyより大きい時にifの処理へ入るようにしてください。
const x = 5;
const y = 3;
if (x > y) {
    console.log('xはyより大きい');
}

//xがyより小さい時にifの処理へ入るようにしてください。
const x = 3;
const y = 5;
if (x < y) {
    console.log('xはyより小さい');
}

//xがy以上の時にifの処理へ入るようにしてください。
const x = 5;
const y = 3;
if (x >= y) {
    console.log('xはy以上');
}

//xがy以下の時にifの処理へ入るようにしてください。
const x = 3;
const y = 5;
if (x <= y) {
    console.log('xはy以下');
}